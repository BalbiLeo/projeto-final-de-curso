    private void enviarASOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarASOActionPerformed
        // Instância da classe de envio de relatório para o e-mail do funcionário
        EnviarPDF ePDF = new EnviarPDF();
        String Email = jL9.getText();
        // Verifica se o relatório de saúde está gerado na tela antes do envio
        if (Email.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Favor gerar relatório na tela.");
        } else {
            // Escolha do relatório a ser enviado para o email do funcionário.
            JFileChooser jfc = new JFileChooser();
            jfc.showOpenDialog(null);
            File Arq = jfc.getSelectedFile();
            String Cam = Arq.getAbsolutePath();
            String Nome = Arq.getName();
            try {
                //Chamada do método de envio de relatório de saúde;
                ePDF.enviarPDF(Email, Cam, Nome);
            } catch (MessagingException | EmailException ex) {
                Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_enviarASOActionPerformed

