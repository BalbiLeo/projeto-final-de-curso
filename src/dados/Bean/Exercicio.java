package dados.Bean;

/**
 *
 * @author Balbicosta
 */
public class Exercicio extends Tratamento{
    
    // Declaração das variáveis da classe Exercicio
    private String forca;
    private String mobil;
    private String potenc;
    private String metab;
    private String exercLeve;
    private String exercMod;
    private String exercIntens;
    private int freqBaixa;
    private int freqMedia;
    private int freqAlta;

    public String getForca() {
        return forca;
    }

    public void setForca(String forca) {
        this.forca = forca;
    }

    public String getMobil() {
        return mobil;
    }

    public void setMobil(String mobil) {
        this.mobil = mobil;
    }

    public String getPotenc() {
        return potenc;
    }

    public void setPotenc(String potenc) {
        this.potenc = potenc;
    }

    public String getMetab() {
        return metab;
    }

    public void setMetab(String metab) {
        this.metab = metab;
    }

    public String getExercLeve() {
        return exercLeve;
    }

    public void setExercLeve(String exercLeve) {
        this.exercLeve = exercLeve;
    }

    public String getExercMod() {
        return exercMod;
    }

    public void setExercMod(String exercMod) {
        this.exercMod = exercMod;
    }

    public String getExercIntens() {
        return exercIntens;
    }

    public void setExercIntens(String exercIntens) {
        this.exercIntens = exercIntens;
    }

    public int getFreqBaixa() {
        return freqBaixa;
    }

    public void setFreqBaixa(int freqBaixa) {
        this.freqBaixa = freqBaixa;
    }

    public int getFreqMedia() {
        return freqMedia;
    }

    public void setFreqMedia(int freqMedia) {
        this.freqMedia = freqMedia;
    }

    public int getFreqAlta() {
        return freqAlta;
    }

    public void setFreqAlta(int freqAlta) {
        this.freqAlta = freqAlta;
    }
}
